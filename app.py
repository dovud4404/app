    logging.info("Ğ£ÑÑ‚Ğ°Ğ½Ğ°Ğ²Ğ»Ğ¸Ğ²Ğ°ĞµĞ¼ webhook â†’ %s", webhook_url)
    bot.set_webhook(webhook_url)

